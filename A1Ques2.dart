/* Q2.
Take two variables and store age then using if/else condition to determine
oldest and youngest among them. */

void main() {
  
  int age1 = 10;
  int age2 = 20;
  
  if (age1 > age2){
    print("$age1 years is older than $age2 years.");
  }
  else {
    print("$age2 years is older than $age1 years.");
  }

}